char	*ft_strcapitalize(char *str)
{
	int		i;
	int		j;
	char	x;

	i = 0;
	j = 1;
	while (str[i] != '\0')
	{
		x = str[i];
		if (j == 1 && x >= 'a' && x <= 'z')
			str[i] -= 32;
		else if (j == 0 && x >= 'A' && x <= 'Z')
			str[i] += 32;
		if (x < '0' || (x > '9' && x < 'A') || (x > 'Z' && x < 'a') || x > 122)
			j = 1;
		else
			j = 0;
		i++;
	}
	return (str);
}
